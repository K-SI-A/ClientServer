    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Supplier supplier = new Supplier();
        supplier.setKodeSupplier(txtKode.getText());
        supplier.setNamaSupplier(txtNama.getText());
        supplier.setAlamat(txtAlamat.getText());
        try {
            int hasil = 0;
            if (isTambahData) { // input data baru
                hasil = supplierDao.save(supplier);
                if (hasil > 0) {
                    // tampilkan pesan berhasil
                    JOptionPane.showMessageDialog(frmAddEditSupplier, "Data supplier berhasil disimpan","Informasi", JOptionPane.INFORMATION_MESSAGE);
                    // tambahkan data barang ke tabel
                    fillToTable(isTambahData, supplier);
                    // tambahkan data barang ke list
                    recordSupplier.add(supplier);
                    // reset form input
                    txtKode.setText("");
                    txtNama.setText("");
                    txtAlamat.setText("");
                    txtKode.requestFocus();
                }
            } else { // update data yg sudah ada
                hasil = supplierDao.update(supplier);
                if (hasil > 0) {
                    // tampilkan pesan berhasil
                    JOptionPane.showMessageDialog(frmAddEditSupplier, "Data barang berhasil disimpan","Informasi", JOptionPane.INFORMATION_MESSAGE);
                    // update data barang yang ada di tabel
                    fillToTable(isTambahData, supplier);
                    // ambil baris barang yang diperbaiki
                    int row = tblSupplier.getSelectedRow();
                    // update data barang yang ada di list
                    recordSupplier.set(row, supplier);
                    // tutup dialog edit data
                    frmAddEditSupplier.setVisible(false);
                }
            }
        } catch (RemoteException ex) {
            Logger.getLogger(FrmBarang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

