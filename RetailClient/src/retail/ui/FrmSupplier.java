    private void btnPerbaikiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPerbaikiActionPerformed
        // TODO add your handling code here:
        int row = tblSupplier.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this,"Data Supplier belum dipilih",
                "Peringatan",JOptionPane.WARNING_MESSAGE);
        }else{
            isTambahData = false;
            Supplier supplier = recordSupplier.get(row);

            txtKode.setText(supplier.getKodeSupplier());
            txtNama.setText(supplier.getNamaSupplier());
            txtAlamat.setText(supplier.getAlamat());

            txtKode.setEnabled(false);
            txtNama.requestFocus();

            frmAddEditSupplier.pack();
            frmAddEditSupplier.setLocationRelativeTo(this);
            frmAddEditSupplier.setModal(true);
            frmAddEditSupplier.setVisible(true);
        }
    }//GEN-LAST:event_btnPerbaikiActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int row = tblSupplier.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this,"Data Barang belum dipilih",
                "Peringatan",JOptionPane.WARNING_MESSAGE);
        }else{
            Supplier supplier = recordSupplier.get(row);
            int result=JOptionPane.showConfirmDialog(this,
                "Apakah data supplier '"+supplier.getNamaSupplier()+"' Ingin dihapus?",
                "Konfirmasi",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);

            if (result == JOptionPane.YES_OPTION) {
                try {
                    result = supplierDao.delete(supplier);
                    if (result > 0) {
                        tableModel.removeRow(row);
                        recordSupplier.remove(supplier);
                    }
                } catch (RemoteException ex) {
                    Logger.getLogger(FrmBarang.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

